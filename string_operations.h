#ifndef STRING_OPERATIONS_H
#define STRING_OPERATIONS_H

/* demo_strlen: regresa la longitud de la cadena s */
int demo_strlen(char *s);

/* demo_strcpy: copia t hacia s */
void demo_strcpy(char *s, char *t);

/* demo_strcmp: regresa < 0 si s < t, 0 si s == t, > 0 si s > t */
int demo_strcmp(char *s, char *t);

#endif
